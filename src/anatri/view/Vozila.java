    private void akcijaDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaDodajNoviActionPerformed
         reset();
        if(naziv.getText().trim().isEmpty()){
            greska(naziv, "Obavezno unijeti naziv");
            return;
        }
       
        if(registracija.getText().trim().isEmpty()){
            greska(registracija, "Obavezno unijeti registraciju");
            return;
        }
        

        Vozilo o = new Vozilo();
        o.setNaziv(naziv.getText().trim());
        o.setRegistracija(registracija.getText().trim());
        o.setTip(tip.getText().trim());
        o.setBoja(boja.getText().trim());
        o.setVrstagoriva(gorivo.getText().trim());
        o.setGrupavozila(grupa.getText().trim());
       
        o=obrada.dodaj(o);

        DefaultListModel<Vozilo> m =(DefaultListModel<Vozilo>) lista.getModel();
        m.addElement(o);
    }//GEN-LAST:event_akcijaDodajNoviActionPerformed

    private void akcijaPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaPromjeniActionPerformed
        if(odabrano==null){
            return;
        }
        reset();
        //kontorle
        if(naziv.getText().trim().isEmpty()){
            greska(naziv, "Obavezno unijeti naziv vozila");
            return;
        }
       
        if(registracija.getText().trim().isEmpty()){
            greska(registracija, "Obavezno unijeti registraciju vozila");
            return;
        }
        odabrano.setNaziv(naziv.getText().trim());
        odabrano.setRegistracija(registracija.getText().trim());
        odabrano.setTip(tip.getText().trim());
        odabrano.setBoja(boja.getText().trim());
        odabrano.setVrstagoriva(gorivo.getText().trim());
        odabrano.setGrupavozila(grupa.getText().trim());

        //promjena u bazi
        if(!obrada.promjeni(odabrano)){
            return;
        }

        //dodavanje/postavljanje
        //nekada ne radi dobro
        lista.repaint();
    }//GEN-LAST:event_akcijaPromjeniActionPerformed

    private void akcijaObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaObrisiActionPerformed
        odabrano = (Vozilo) lista.getSelectedValue();
        if(odabrano==null){
            return;
        }
        if(JOptionPane.showConfirmDialog(getParent(), "Jeste li sigurni da želite obrisati " + odabrano.getNaziv() + "?","Brisanje vozila",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==JOptionPane.NO_OPTION){
            return;
        }

        if(!obrada.obrisi(odabrano)){
            return;
        }

        DefaultListModel<Vozilo> m = (DefaultListModel<Vozilo>) lista.getModel();
        m.removeElement(odabrano);
        lista.repaint();
        cistiPolja();
    }//GEN-LAST:event_akcijaObrisiActionPerformed

    private void cistiPolja(){
        naziv.setText("");
        registracija.setText("");
        tip.setText("");
        gorivo.setText("");
        boja.setText("");
        gorivo.setText("");
        grupa.setText("");        
    }
     private void reset() {
       
        for (Component c : this.getContentPane().getComponents()) {
            if (c instanceof JTextField || c instanceof JPasswordField){
                JComponent jc = (JComponent) c;
                jc.setBackground(Color.white);
                jc.setToolTipText("");
            }
        }
    }
      private void greska(JComponent komponenta, String obavijest){
           komponenta.setBackground(Color.red);
           komponenta.requestFocus();
           komponenta.setToolTipText(obavijest);
           
    }


