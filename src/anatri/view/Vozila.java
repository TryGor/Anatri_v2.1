    private void akcijaTraziVozilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaTraziVozilaActionPerformed
        
        cistiPolja();
        
        List<Vozilo> vozila = obrada.
                dohvati(uvjet.getText());
       
        DefaultListModel<Vozilo> m = new DefaultListModel<>();
        for (Vozilo vozilo : vozila){
                m.addElement(vozilo);
        }
        lista.setModel(m);
    }//GEN-LAST:event_akcijaTraziVozilaActionPerformed

    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        odabrano = (Vozilo) lista.getSelectedValue();
        if(odabrano==null){
            return;
        }
        naziv.setText(odabrano.getNaziv());
        registracija.setText(odabrano.getRegistracija());
        tip.setText(odabrano.getTip());
        boja.setText(odabrano.getBoja());
        gorivo.setText(odabrano.getVrstagoriva());
        grupa.setText(odabrano.getGrupavozila());
    }//GEN-LAST:event_listaValueChanged

